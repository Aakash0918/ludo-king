            <p>In line with the provisions contained in <a
                    href="https://incometaxindia.gov.in/news/finance-bill-2023-highlights.pdf">Finance Act, 2023</a>,
                following TDS policy is applicable for all players on Ludo king from 1 Apr, 2023.</p>
            <ul>
                <li>
                    <p>TDS is applicable at the time of withdrawal or deposit refund</p>
                </li>
                <li>
                    <p>30% TDS is applicable on any positive net winnings at the time of withdrawal</p>
                </li>
                <li>
                    <p>TDS is also applicable at the end of financial year on year end wallet balance by considering it
                        as a withdrawal amount. Post applying TDS on the remaining balance (as per policy), the
                        remaining amount will be carried forward to the next financial year as deposit balance. Carried
                        forward balance will be considered as investment for next year and TDS would not be applicable
                        for that amount.</p>
                </li>
            </ul>
            <p>Here, Net winnings = Total Withdrawals - Total Deposits (in a financial year). Apr 1 to Mar 31 duration
                is considered a financial year.</p>
            <h4>How was TDS deducted before Apr 1, 2023 :</h4>
            <ul>
                <li>
                    <p>Before Apr 1 2023, TDS was deducted at a game level when the winnings were greater than 10K.</p>
                </li>
            </ul>
            <p>Example : Previously if I won Rs 11000 in a game, then 30% TDS will be deducted i.e. 3300 and hence Rs
                7700 will be credited in the withdrawal wallet.</p>
            <h4>How is the current TDS policy different from the last TDS policy of FY 2022- 2023</h4>
            <ul>
                <li>
                    <p>Post Apr 1, if I win some amount let's assume Rs 11000, NO TDS will be deducted while crediting
                        that amount in the withdrawal wallet. Entire Rs 11000 will be transferred to the winnings
                        wallet.</p>
                </li>
                <li>
                    <p>Only when the user will try to withdraw any amount from the wallet, we will look at the net
                        winnings and basis that calculate the TDS applicable.</p>
                </li>
                <li>
                    <p>Hence we will deduct TDS only when the withdrawal running total is more than the Deposits. If the
                        user is in loss, there will be no TDS. Hence this will be beneficial to the users.</p>
                </li>
            </ul>
            <p>Please note that the TDS Policy is based on the understanding of the provisions as introduced/ amended by
                Finance Act, 2023 and the Company reserves its right to modify/ change/ amend the TDS Policy basis law
                applicable at the relevant time. Attached is the finance bill amendment which says that the policy will
                be applicable from Apr 1, 2023 rather than July 1, 2023.</p><br>
            <h4>What Does It Mean For Players?</h4>
            <p class="decoration-none">Find below a few scenarios to understand the application of the aforementioned
                TDS Policy.</p>
            <p><b>Scenario 1:</b> If your net winnings (including the withdrawal amount) is more than 0, then 30% TDS
                will be deducted on the net winnings at the time of withdrawal.</p>
            <p><b>Example:</b></p>
            <table class="table border table-hover">
                <tbody>
                    <tr>
                        <td>Total withdrawals in the financial year(A)</td>
                        <td>₹5,000</td>
                    </tr>
                    <tr>
                        <td>Total deposits in the financial year(B)</td>
                        <td>₹10,000</td>
                    </tr>
                </tbody>
            </table>
            <p>In the above scenario, if you want to withdraw ₹7,000 , then the net winnings will be:</p>
            <table class="table border table-hover">
                <tbody>
                    <tr>
                        <td>Amount being withdrawn by the player(C)</td>
                        <td>₹7,000</td>
                    </tr>
                    <tr>
                        <td>Net winnings(A+C - B)</td>
                        <td>₹2,000</td>
                    </tr>
                    <tr>
                        <td>30% TDS Applicable on Net Winnings(D)</td>
                        <td>₹600</td>
                    </tr>
                    <tr>
                        <td>Amount to be credited in the bank account(C-D)</td>
                        <td>₹6,400</td>
                    </tr>
                </tbody>
            </table>
            <p><b>Zero TDS Withdrawal amount:</b> If you withdraw upto ₹5,000 , then there will be zero TDS deducted as
                net winnings will be ₹0 (₹5,000 + ₹5,000 - ₹10,000). See calculation below:</p>
            <table class="table border table-hover">
                <tbody>
                    <tr>
                        <td>Total withdrawals in the financial year(A)</td>
                        <td>₹5,000</td>
                    </tr>
                    <tr>
                        <td>Total deposits in the financial year(B)</td>
                        <td>₹10,000</td>
                    </tr>
                    <tr>
                        <td>Amount being withdrawn by the player(C)</td>
                        <td>₹5,000</td>
                    </tr>
                    <tr>
                        <td>Net winnings(A+C - B)</td>
                        <td>₹0</td>
                    </tr>
                </tbody>
            </table>
            <p><b>Scenario 2:</b> If you have paid TDS in previous withdrawals, then no TDS is applicable on withdrawals
                till your net winnings crosses the amount for which you have already paid TDS.</p>
            <p><b>Example:</b></p>
            <table class="table border table-hover">
                <tbody>
                    <tr>
                        <td>Total withdrawals in the financial year</td>
                        <td>₹20,000</td>
                    </tr>
                    <tr>
                        <td>Total deposits in the financial year</td>
                        <td>₹20,000</td>
                    </tr>
                    <tr>
                        <td>
                            <p>TDS paid in the financial year</p>
                            <p>(TDS paid on winnings of ₹6,000 as initially you had positive net winnings of ₹6,000)</p>
                        </td>
                        <td>₹1,800</td>
                    </tr>
                </tbody>
            </table>
            <p>In this scenario, No TDS applicable till your net winnings crosses ₹6,000 (winnings already taxed):</p>
            <ul>
                <li>
                    <p><b>Zero TDS Withdrawal amount:</b> If you withdraw up to ₹6,000, no TDS is applicable</p>
                </li>
                <li>
                    <p>If you withdraw ₹10, 000, then 30% TDS is applicable only on ₹4,000 winnings i.e. ₹1,200.</p>
                </li>
            </ul>
            <p><b>Scenario 3:</b> You have paid TDS on initial winnings during the financial year but your net winnings
                have reduced post that.</p>
            <p><b>Example:</b></p>
            <table class="table border table-hover">
                <tbody>
                    <tr>
                        <td>Total withdrawals in the financial year(A)</td>
                        <td>₹40,000</td>
                    </tr>
                    <tr>
                        <td>Total deposits in the financial year(B)</td>
                        <td>₹20,000</td>
                    </tr>
                    <tr>
                        <td>Net winnings in the financial year(A-B)</td>
                        <td>₹20,000</td>
                    </tr>
                    <tr>
                        <td>TDS paid till date</td>
                        <td>₹6,000</td>
                    </tr>
                </tbody>
            </table>
            <p>Post this, if your net winnings reduces to ₹10,000, then, subject to applicable law, the excess TDS
                deducted can be claimed as refund in your annual income tax filing.</p>
            <p><b>Scenario 4:</b> At the end of the financial year, if you have an amount in your account, then TDS will
                be calculated on that amount by considering it as a withdrawal of the financial year and remaining
                amount will be carried forward to next year as starting deposit amount. Withdrawal from the starting
                deposit amount will not be subject to TDS in the next year.</p>
            <p><b>Example:</b></p>
            <table class="table border table-hover">
                <tbody>
                    <tr>
                        <td>Total withdrawals in the financial year(A)</td>
                        <td>₹40,000</td>
                    </tr>
                    <tr>
                        <td>Total deposits in the financial year(B)</td>
                        <td>₹40,000</td>
                    </tr>
                    <tr>
                        <td>
                            <p>TDS paid till date(C)</p>
                            <p>(on winning amount of ₹5,000 as initially you had positive net winnings of ₹5,000)</p>
                        </td>
                        <td>₹1,500</td>
                    </tr>
                    <tr>
                        <td>Account balance at the end of Mar 31, 2024(D)</td>
                        <td>₹30,000</td>
                    </tr>
                </tbody>
            </table>
            <p>In above scenario, following calculation is done:</p>
            <table class="table border table-hover">
                <tbody>
                    <tr>
                        <td>Total withdrawals in the financial year(A+D)</td>
                        <td>₹70,000</td>
                    </tr>
                    <tr>
                        <td>Total deposits in the financial year(B)</td>
                        <td>₹40,000</td>
                    </tr>
                    <tr>
                        <td>Net winnings(A+D-B)</td>
                        <td>₹30,000</td>
                    </tr>
                    <tr>
                        <td>30% TDS Applicable on Net Winnings(E)</td>
                        <td>₹9,000</td>
                    </tr>
                    <tr>
                        <td>TDS paid till date(C)</td>
                        <td>₹1,500</td>
                    </tr>
                    <tr>
                        <td>TDS Remaining to be paid(E-C)</td>
                        <td>₹7,500</td>
                    </tr>
                </tbody>
            </table>
            <p>Thus, ₹7,500 TDS is deducted from your account balance at the end of Mar 31, 2024 and remaining balance
                of ₹22,500 will be carried forward to the next financial year as deposit amount. You will not have to
                pay TDS in the next financial year on withdrawal out of this amount.</p>
            <p><b>Special Note:</b> In case you have an amount on the table at the end of the financial year, then it
                will not be considered in next year's starting balance.</p>
