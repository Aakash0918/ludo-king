    <h4>Guide to Responsible Play</h4>
    <p>Sometimes, players may find it hard to recognize that their online play is getting out of control. A common
        reaction is to minimize, hide or deny such problems and the harm it could be causing. Some people will lie to
        themselves about how much money or time is being spent on online playing.</p>
    <h6>Following are some of the best practices to help you play responsibly:</h6>
    <ul>
        <li>Play in moderation and only for entertainment.</li>
        <li>Do not play to make money or escape problems.</li>
        <li>Never chase your losses while playing.</li>
        <li>Set aside an entertainment budget.</li>
        <li>Keep track of the time and monitor the amount of money you spend.</li>
        <li>Use the Add Cash limit option to help control the amount you spend.</li>
        <li>Balance the time you spend on playing online games with other leisure activities.</li>
    </ul>
    <h4>Recognize whether you are not playing responsibly</h4>
    <h6>Do you do any of the following:</h6>
    <ul>
        <li>Do you spend more money and time than you intend to playing games?</li>
        <li>Do you feel guilty or ashamed while game playing habits?</li>
        <li>Do you try to win back your losses?</li>
        <li>Do you miss important things in life such as family time, work, leisure activities, appointments to play
            games?</li>
        <li>Do you think about your gameplay all day long?</li>
        <li>Do you have arguments with friends or family about you playing habits?</li>
        <li>Do you lie or steal to get money to play games?</li>
        <li>Do you have debts or struggled financially to play games?</li>
        <li>Has playing games negatively impacted your professional life?</li>
        <li>Do you have relationship problems like arguments, disagreements or loss of Connection with friends and
            family?</li>
        <li>Have you experienced an increase in stress, depression or panic attacks due to playing games?</li>
    </ul>
